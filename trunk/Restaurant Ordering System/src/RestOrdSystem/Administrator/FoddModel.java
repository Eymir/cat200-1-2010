package RestOrdSystem.Administrator;

public class FoddModel {
	
	
	private String foodName;
	private String foodType;
	private double foodPrice;
	private int foodQoh;
	private int foodMin;
	private String picture;
	private String description;
	
	public String getFoodName() {
		return foodName;
	}
	public void setFoodName(String foodName) {
		this.foodName = foodName;
	}
	public String getFoodType() {
		return foodType;
	}
	public void setFoodType(String foodType) {
		this.foodType = foodType;
	}
	public double getFoodPrice() {
		return foodPrice;
	}
	public void setFoodPrice(double foodPrice) {
		this.foodPrice = foodPrice;
	}
	public int getFoodQoh() {
		return foodQoh;
	}
	public void setFoodQoh(int foodQoh) {
		this.foodQoh = foodQoh;
	}
	public int getFoodMin() {
		return foodMin;
	}
	public void setFoodMin(int foodMin) {
		this.foodMin = foodMin;
	}
	public String getPicture() {
		return picture;
	}
	public void setPicture(String picture) {
		this.picture = picture;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	
	

	
	
	
	
	
	

}
